#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	newl;
	int		i;

	i = 0;
	newl = '\n';
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, &newl, 1);
}
